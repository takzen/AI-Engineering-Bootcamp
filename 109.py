# 3. Niezbędnik AI Engineera: Skąd Czerpać Wiedzę?
# ==============================================================================

"""
Świat AI pędzi. Poniżej znajduje się sprawdzona lista źródeł, które pozwolą Ci
być zawsze na bieżąco. Wybierz kilka i śledź je regularnie.

**Newslettery (Twoja cotygodniowa pigułka wiedzy):**
-   **TLDR AI:** Codzienny, bardzo zwięzły przegląd najważniejszych newsów i prac badawczych. Absolutny must-have.
-   **The Batch (DeepLearning.AI):** Cotygodniowy newsletter od Andrew Ng, skupiony na ważnych trendach.
-   **AlphaSignal:** Skupia się na praktycznych zastosowaniach AI i najciekawszych projektach z GitHuba.

**Kanały na YouTube (Wiedza w formie wideo):**
-   **LangChain:** Oficjalny kanał z tutorialami, nowościami i studiami przypadków.
-   **James Briggs:** Mnóstwo praktycznych tutoriali na temat budowania aplikacji z LLM.
-   **Prompt Engineering (kanał prowadzony przez Daliana):** Praktyczne porady dotyczące budowy systemów AI.

**Blogi i Społeczności:**
-   **Blogi firm AI:** Oficjalne blogi OpenAI, Anthropic, Google DeepMind, Mistral AI. Tam najpierw pojawiają się informacje o przełomowych modelach.
-   **Hugging Face Blog:** Kopalnia wiedzy o modelach open-source, tutorialach i nowych technikach.
-   **Twitter (X) / AI Twitter:** Śledź kluczowe postacie w świecie AI (np. Andrej Karpathy, Harrison Chase, Andrew Ng, Yann LeCun). To tam najszybciej pojawiają się gorące dyskusje i przecieki.
-   **Reddit:** Subreddity takie jak r/LocalLLaMA (dla entuzjastów modeli open-source), r/MachineLearning, r/LangChain.
"""

