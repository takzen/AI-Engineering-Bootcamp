# 2. Twój Następny Projekt – Co Dalej?
# ==============================================================================

"""
Nie spoczywaj na laurach. Najlepszym sposobem na utrwalenie wiedzy jest natychmiastowe
zastosowanie jej w nowym kontekście.

Pomysły na Twoje kolejne projekty (o rosnącym poziomie trudności):
-   **Rozbuduj obecny projekt:** Zrealizuj punkty z sekcji "Możliwe dalsze kierunki rozwoju"
    z Twojego pliku README. Zapakuj aplikację w Dockera, wdróż ją na chmurze (np. AWS, Heroku, Streamlit Cloud).
    To niezwykle cenna umiejętność!

-   **Zbuduj aplikację z Agentem (AI Agent):** Zamiast prostego Q&A, stwórz agenta,
    który może korzystać z narzędzi (np. wyszukiwarki internetowej, kalkulatora, API pogodowego).
    Zgłęb temat "LangChain Agents". To kolejny, wyższy poziom abstrakcji.

-   **Fine-tuning małego modelu:** Spróbuj dostroić (fine-tune) mniejszy, open-source'owy
    model językowy (np. z rodziny Llama 3 lub Mistral) na specyficznym zbiorze danych.
    To pozwoli Ci zrozumieć, co dzieje się "pod maską" wielkich modeli.

-   **Zbuduj aplikację multimodalną:** Stwórz system, który rozumie nie tylko tekst, ale i obrazy.
    Np. aplikacja, do której wrzucasz zdjęcie produktu, a ona generuje jego marketingowy opis.
    Zbadaj możliwości modeli takich jak GPT-4o czy LLaVA.
"""

